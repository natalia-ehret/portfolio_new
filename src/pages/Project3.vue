   <p>My project is an attempt to reprocess this childhood trauma: to visualize how I perceive these old things now, to digest my post-Soviet childhood. I reflect not only on the trauma of poverty in clothing choices, but also on the trauma of school uniforms, strictly gendered snowflake and bunny New Year costumes, and the difficulty to show my individuality. In the fashion pictures, I try to aestheticise the 'old' clothes that many of my generation and culture and I have complicated, painful feelings about. It helps me realise my past and no longer see it in terms of a victim, it removes the resentment I felt towards my parents, time and circumstances.</p>
